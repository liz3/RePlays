const StackPane = ({ meta, type, stack, latestLeagueVersion, champion }) => {
  const st = useMemo(() => {
    return [{ meta, type }, ...(stack || [])].filter(
      (e) => e.meta !== undefined && e.meta !== null,
    );
  }, [stack, meta]);
  let className = 'bookmark-stackpane';
  if(!st.length)
    return null;
  return (
    <div className={className}>
      {st.map((entry, i) => (
        <div
          key={i}
          className={'flex w-full justify-between'}
          style={{
            background:
              entry.meta && entry.meta.killerName && champion === entry.meta.killerName
                ? '#ccb574'
                : entry.meta.team === 0
                  ? '#3b213b'
                  : '#172453',
            padding: '4px',
          }}
        >
          {entry.meta && entry.meta.killerName ? (
            <img
              className=''
              src={`https://ddragon.leagueoflegends.com/cdn/${latestLeagueVersion}/img/champion/${entry.meta.killerName}.png`}
              alt={entry.meta.killerName}
            />
          ) : null}
          {entry.type !== BookmarkType.Kill ? <HoverIcon type={BookmarkType.Kill} /> : null}
          <HoverIcon type={entry.type} />
          {entry.meta && entry.meta.victimChamp ? (
            <img
              className=''
              src={`https://ddragon.leagueoflegends.com/cdn/${latestLeagueVersion}/img/champion/${entry.meta.victimChamp}.png`}
              alt={entry.meta.victimChamp}
            />
          ) : null}
        </div>
      ))}
    </div>
  );
};

